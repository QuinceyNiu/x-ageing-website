// Infinite float loop animation
export function floatLoop(delay: number = 0): TargetAndTransition {
  return {
    y: [0, -18, 0],
    opacity: [0.6, 1, 0.6],
    transition: {
      duration: 6 + delay * 0.5,
      repeat: Infinity,
      ease: "easeInOut" as const,
      delay,
    },
  };
}
